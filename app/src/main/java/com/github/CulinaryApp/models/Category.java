package com.github.CulinaryApp.models;

public class Category {
    String name;
    String image;

    public Category() {

    }

    public void setName(String name) {
        this.name = name;
    }
    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return this.name;
    }
    public String getImage() {
        return this.image;
    }
}
